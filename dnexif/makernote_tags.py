def get_makernote_tag_name(maker: str, tag_id: int) -> Optional[str]:
    """
    Get MakerNote tag name for a given manufacturer and tag ID.
    
    Args:
        maker: Camera manufacturer name
        tag_id: Tag ID
        
    Returns:
        Tag name or None if not found
    """
    maker_upper = maker.upper()
    if maker_upper in MAKERNOTE_TAGS:
        return MAKERNOTE_TAGS[maker_upper].get(tag_id)
    return None


