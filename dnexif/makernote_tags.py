def get_canon_sub_ifd_tag_name(parent_tag_id: int, sub_tag_id: int) -> Optional[str]:
    """
    Get Canon sub-IFD tag name (standard format - direct tag name).
    
    Args:
        parent_tag_id: Parent tag ID (e.g., 0x0001 for CanonCameraSettings)
        sub_tag_id: Sub-tag ID within the sub-IFD
        
    Returns:
        Tag name in standard format (e.g., "MacroMode") or None if not found
    """
    if parent_tag_id in CANON_SUB_IFD_TAGS:
        return CANON_SUB_IFD_TAGS[parent_tag_id].get(sub_tag_id)
    return None

